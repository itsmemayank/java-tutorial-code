
public class Car {

    private String name;
    private String model;
    private int year;

    public void setName(String name) {
        this.name = name;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getName() {
        return this.name;
    }

    public String getModel() {
        return this.model;
    }

    public int getYear() {
        return this.year;
    }

}