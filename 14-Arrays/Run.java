
public class Run {
    public static void main(String[] args) {

        // Arrays - used to store multiple values in a single variable

        // Array Declaration
        String[] cars = new String[3];

        // Inserting Elements into Array
        cars[0] = "BMW";
        cars[1] = "Mercedes";
        cars[2] = "Tesla";

        // Display Array
        for (int i = 0; i < cars.length; i++) {
            System.out.println(cars[i]);
        }

    }
}
